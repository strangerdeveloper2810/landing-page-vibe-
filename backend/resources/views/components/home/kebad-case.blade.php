    (function(){
        var section = document.getElementById('capabilities-section');
        if (!section) { return; }

        // Intersection reveal
        var revealed = false;
        try {
            var io = new IntersectionObserver(function(entries){
                entries.forEach(function(entry){
                    if (entry.isIntersecting && !revealed) {
                        revealed = true;
                        section.classList.add('is-visible');
                    }
                });
            }, { threshold: 0.15 });
            io.observe(section);
        } catch (e) {
            // Fallback: immediate show
            section.classList.add('is-visible');
        }

        // Re-trigger animations when switching tabs
        var tabs = document.querySelectorAll('#capabilities-tabs button[data-bs-toggle="tab"]');
        tabs.forEach(function(btn){
            // pre-switch: fade previous image down gently
            btn.addEventListener('show.bs.tab', function(ev){
                var prevBtn = ev.relatedTarget;
                if (!prevBtn) { return; }
                var prevSel = prevBtn.getAttribute('data-bs-target');
                var prevPane = document.querySelector(prevSel);
                if (prevPane) {
                    prevPane.classList.add('fading-out');
                    setTimeout(function(){ prevPane.classList.remove('fading-out'); }, 1200);
                }
            });

            // post-switch: animate new pane contents
            btn.addEventListener('shown.bs.tab', function(ev){
                var targetSel = ev.target.getAttribute('data-bs-target');
                var pane = document.querySelector(targetSel);
                if (!pane) { return; }
                // Remove any previous flag to restart animations
                pane.classList.remove('just-shown');
                // Force reflow
                void pane.offsetWidth;
                // Add flag to trigger CSS animations
                pane.classList.add('just-shown');
                // Clean up after animations end (optional)
                setTimeout(function(){ pane.classList.remove('just-shown'); }, 2000);
            });
        });
    })();

